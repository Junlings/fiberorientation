function [ c_mean, c_std ] = cal_stress( ga,k2 )
%UNTITLED2 Summary of this function goes here
%   Detailed explanation goes here


c_mean = 0.1250000000e1 / (1 + k2 ^ (1 - ga)) * gamma((k2 ^ (-cos(ga) ^ 4))...
    / 0.2e1 + 0.1e1) / gamma((k2 ^ (-cos(ga) ^ 4)) / 0.2e1 + 0.1e1...
    / 0.2e1) * gamma((k2 ^ (-cos(ga) ^ 4)) / 0.2e1 + 0.1e1 +...
    (k2 ^ (-sin(ga) ^ 4)) / 0.2e1) / gamma((k2 ^ (-cos(ga) ^ 4))...
    / 0.2e1 + 0.3e1 / 0.2e1 + (k2 ^ (-sin(ga) ^ 4)) / 0.2e1);

c_std = 0.2828427125e0 * sqrt(pi / (1 + k2 ^ (1 - ga)) *...
    (0.2083333333e-1 * (k2 ^ (-cos(ga) ^ 4) + 1) /...
    (k2 ^ (-cos(ga) ^ 4) + 2 + k2 ^ (-sin(ga) ^ 4)) -...
    0.1562500000e-1 * gamma((k2 ^ (-cos(ga) ^ 4)) / 0.2e1 + 0.1e1) ^...
    2 / gamma((k2 ^ (-cos(ga) ^ 4)) / 0.2e1 + 0.1e1 / 0.2e1) ^ 2 *...
    gamma((k2 ^ (-cos(ga) ^ 4)) / 0.2e1 + 0.1e1 + (k2 ^ (-sin(ga) ^ 4))...
    / 0.2e1) ^ 2 / gamma((k2 ^ (-cos(ga) ^ 4)) / 0.2e1 + 0.3e1 /...
    0.2e1 + (k2 ^ (-sin(ga) ^ 4)) / 0.2e1) ^ 2));

end

